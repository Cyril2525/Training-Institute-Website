$('#schedule').click(function(){
    $('.Ad_div').show();
    $('.gmat_slider_div').hide();
    $('.footer_section').hide();
    $('.fees_div').hide();
    $('.gmat_body').hide();
    $('.header_section').hide();
 });
 
 $('#Ad_button').click(function(){
    $('.Ad_div').hide();
    $('.gmat_slider_div').show();
    $('.footer_section').show();
    $('.fees_div').show();
    $('.gmat_body').show();
    $('.header_section').show();
 });
 